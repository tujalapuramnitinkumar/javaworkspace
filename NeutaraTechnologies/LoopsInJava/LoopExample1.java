// This program demonstrates a simple for loop in Java
class LoopExample1 {
    public static void main(String[] args) {
        // Using a for loop to print numbers from 1 to 5
        for (int i = 1; i <= 5; i++) {
            System.out.println("Number: " + i);
        }
    }
}
/*Output:
Number: 1
Number: 2
Number: 3
Number: 4
Number: 5
*/ 
