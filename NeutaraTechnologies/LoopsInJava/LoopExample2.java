// Example of a while loop in Java 
class LoopExample2 {
    public static void main(String[] args) {
        int i = 1;
        // Using a while loop to print numbers from 1 to 5
        while (i <= 5) {
            System.out.println("Number: " + i);
            i++;
            
        }
    }
}
// Output:
 // Number: 1
 // Number: 2
 // Number: 3
 // Number: 4
 // Number: 5
 // Key Points:
/*A while loop continues to execute as long as its condition is true.
The loop variable (i) is initialized before the loop starts.
The loop variable is updated (incremented) inside the loop to avoid infinite loops.
The entry point is the main() method.
Statements end with a semicolon (;).
Curly braces {} define blocks of code.
The entry point is the main() method.    */ 


